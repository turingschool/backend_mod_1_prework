# Printing a simple string
puts "I will now count my chickens:"

# String interpolation for simple math, via operators
puts "Hens #{25 + 30 / 6.0}"
puts "Roosters #{100 - 25 * 3 % 4.0}"

# Printing another string
puts "Now I will count the eggs:"

# Printing simple math, via operators
puts 3 + 2 + 1 - 5 + 4 % 2 - 1 / 4 + 6.0

# Printing yet another string
puts "Is it true that 3 + 2 < 5 - 7?"

# Printing a boolean
puts 3 + 2 < 5 - 7.0

# More string interpolation with math
puts "What is 3 + 2? #{3 + 2.0}"
puts "What is 5 - 7? #{5-7.0}"

# Printing another string
puts "Oh, that's why it's false."

# And printing yet another string
puts "How about some more."

# String interpolation with booleans
puts "Is it greater? #{5 > -2.0}"
puts "Is it greater or equal? #{5 >= -2.0}"
puts "Is it less or equal? #{5 <= -2.0}"
