#takes two arguments
def print_two(arg1, arg2)
  puts "arg1: #{arg1}, arg2: #{arg2}"
end

#takes one argument
def print_one(arg1)
  puts "arg1: #{arg1}"
end

#no arguments
def print_none()
  puts "I got nothin'."
end

print_two("Aidan", "McGuire")
print_one("First!")
print_none()

