class Burrito
  attr_reader :protein, :base, :toppings
  def initialize(protein, base, toppings)
    @protein  = protein
    @base     = base
    @toppings = toppings
  end

  def add_topping
    puts "What toppings would you like to add?"
  end

  def remove_topping
    puts "Which toppings would you like to remove?"
  end

  def change_protein
    puts "Would you like to add or remove a protein?"
  end
end

