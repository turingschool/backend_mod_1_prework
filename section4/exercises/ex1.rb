# What are objects

# Objects are created from classes. Think of classes as molds and objects as
# the things you produce out of those molds. Individual objects will contain
# different information from other objects, yet they are instances of the same
# class. Here's an example of two objects of the String class:

"hello".class
"world".class

# In the above example, we use the class instance method to determine what the
# class is for each object. As you can see, everything we've been using, from
# strings to integers, are in fact objects, which are instantiated from a class. 
